{
	OLED_WriteByte(0X8D, OLED_CMD);
	OLED_WriteByte(0X10, OLED_CMD);
	OLED_WriteByte(0XAE, OLED_CMD);
}

/*********************************************************************
  * @brief  OLED Refresh Gram
  * @param  None
  * @retval None
	* @date   20141204
***********************************************************************/
void OLED_Refresh_Gram(void)
{
	uint8_t i, n;
	for(i = 0; i < 8; i ++)
	{
		OLED_WriteByte(0xb0 + i, OLED_CMD);
		OLED_WriteByte(0x00, OLED_CMD);
		OLED_WriteByte(0x10, OLED_CMD);
		for(n = 0; n < 128; n ++)
		{
			OLED_WriteByte(OLED_GRAM[n][i], OLED_DAT);
		}
	}
}

/*********************************************************************
  * @brief  OLED Clear Screen.
  * @param  None
  * @retval None
	* @date   20141204
***********************************************************************/
void LCD_Clear(uint8_t Color)
{
	uint8_t i, n;

	for(i = 0; i < 8; i ++)
	{
		for(n = 0; n < 128; n ++)
		{
			OLED_GRAM[n][i] = Color;
		}
	}

	OLED_Refresh_Gram();
}
/*********************************************************************
  * @brief  OLED Draw Point.
  * @param  X:
  *   This parameter can be one of the following values:
  *     @arg X: where x can be 0..127
  * @param  Y:
  *   This parameter can be one of the following values:
	* @param  uint8_t Fill :
  *     @arg where Fill can be 0 or 1
  * @retval None
	* @date   20141204
***********************************************************************/
void OLED_DrawPoint(uint8_t Xpos,uint8_t Ypos,uint8_t Fill)
{
	uint8_t Pos, Bx, Temp = 0;

	if(Xpos > 127 || Ypos > 63)
		return;
	Pos = 7 - Ypos / 8;
	Bx = Ypos % 8;
	Temp = 1 << (7 - Bx);
	if(Fill)
	{
		OLED_GRAM[Xpos][Pos] |= Temp;
	}
	else
	{
		OLED_GRAM[Xpos][Pos] &= ~Temp;
	}
//	OLED_Refresh_Gram();
}


/**********************************************************************
  * @brief  Displays one character
  *
  * @param  X:
  *   This parameter can be one of the following values:
  *     @arg X: where x can be 0..127
  * @param  Y:
  *   This parameter can be one of the following values:
  *     @arg Y: where y can be 0..63
  * @param  Chr: character ascii code,must be between 0x20 and 0x7e.
  * @param  Size: specifies the size of the font(12 or 16)
  * @param Mode: whether reverses the character
  *   This paramter can be one of following value;
  *   @arg Mode: where Mode can be 0 or 1
  * @retval None
  * @date   20141204
**********************************************************************/

void OLED_ShowChar(uint8_t X, uint8_t Y, uint8_t Chr, uint8_t Size, uint8_t Mode)
{
	uint8_t Temp, t, t1;
	uint8_t y0 = Y;

	Chr = Chr - ' ';
    for(t = 0; t < Size; t ++)
    {
			if(Size == 12)
			{
				Temp = OLED_Asc2_1206[Chr][t];
			}
			else
			{
				Temp = OLED_Asc2_1608[Chr][t];
			}
			for(t1 = 0; t1 < 8; t1 ++)
			{
				if(Temp & 0x80)
					OLED_DrawPoint(X, Y, Mode);
				else
					OLED_DrawPoint(X, Y, !Mode);
					Temp <<= 1;
					Y ++;
					if((Y - y0) == Size)
					{
						Y = y0;
						X ++;
						break;
					}
			}
    }
//	OLED_Refresh_Gram();
}

/**********************************************************************
  * @brief  Displays character string
  *
  * @param  X:
  *   This parameter can be one of the following values:
  *     @arg X: where x can be 0..127
  * @param  Y:
  *   This parameter can be one of the following values:
  *     @arg Y: where y can be 0..63
  * @param  *Str: .character string
  * @retval None
  * @date   20141204
**********************************************************************/
void OLED_ShowString(uint8_t X, uint8_t Y, const char *Str, uint8_t fontsize)
{
#define MAX_CHAR_POSX 122
#define MAX_CHAR_POSY 58

    while(*Str != '\0')
    {
      if(X > MAX_CHAR_POSX)
		  {
			   X = 0;
			   Y += 16;
		  }
      if(Y > MAX_CHAR_POSY)
		  {
			   Y = X = 0;
			   LCD_Clear(0x00);
		  }
      OLED_ShowChar(X, Y, *Str, fontsize, 1);
	  if(fontsize==16)
		  X += 8;
	  else
		  X += 6;
      Str ++;
    }
		OLED_Refresh_Gram();
}
